<?php
function i18n_init(): void
{
    if (session_status() === PHP_SESSION_NONE) {
        session_start();
    }
    $allowed = ['en', 'ne'];
    if (isset($_GET['lang']) && in_array($_GET['lang'], $allowed, true)) {
        $_SESSION['lang'] = $_GET['lang'];
    }
    if (!isset($_SESSION['lang']) || !in_array($_SESSION['lang'], $allowed, true)) {
        $_SESSION['lang'] = 'en';
    }
}

i18n_init();

function i18n_lang(): string
{
    return $_SESSION['lang'] ?? 'en';
}

function i18n_t(string $key, string $fallback = ''): string
{
    $translations = i18n_dict();
    $lang = i18n_lang();
    if (isset($translations[$lang][$key])) {
        return $translations[$lang][$key];
    }
    return $fallback !== '' ? $fallback : $key;
}

function i18n_url_with_lang(string $lang, ?string $requestUri = null): string
{
    $requestUri = $requestUri ?? ($_SERVER['REQUEST_URI'] ?? '');
    $parsed = parse_url($requestUri);
    $path = $parsed['path'] ?? '';

    if ($path === '') {
        $path = $_SERVER['PHP_SELF'] ?? '';
    }

    $query = [];
    if (!empty($parsed['query'])) {
        parse_str($parsed['query'], $query);
    }

    $query['lang'] = $lang;
    $queryString = http_build_query($query);

    return $path . ($queryString !== '' ? '?' . $queryString : '');
}

function i18n_status_label(string $status): string
{
    $status = strtolower(trim($status));
    return i18n_t('status.' . $status, ucfirst($status));
}

function i18n_role_label(string $role): string
{
    $role = strtolower(trim($role));
    $fallback = ucwords(str_replace('_', ' ', $role));
    return i18n_t('role.' . $role, $fallback);
}

function i18n_priority_label(string $priority): string
{
    $priority = strtolower(trim($priority));
    return i18n_t('priority.' . $priority, ucfirst($priority));
}

